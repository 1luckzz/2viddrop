function buildSectionsHTML(sections) {
  return sections.map(([title, body]) => {
    const lines = body.split('\n');
    const bodyHtml = lines.map(line => {
      if (line.startsWith('•')) {
        return `<li>${line.slice(2)}</li>`;
      }
      return `<p>${line}</p>`;
    });
    // Wrap li items in ul
    let html = '';
    let inList = false;
    bodyHtml.forEach(item => {
      if (item.startsWith('<li>')) {
        if (!inList) { html += '<ul>'; inList = true; }
        html += item;
      } else {
        if (inList) { html += '</ul>'; inList = false; }
        html += item;
      }
    });
    if (inList) html += '</ul>';
    return `<h2>${title}</h2>${html}`;
  }).join('');
}

function initLegalPage(type) {
  // Setup lang switcher
  buildLangSwitcher();
  applyTranslations();

  // Render content
  renderLegalContent(type);

  // Re-render on lang change
  const origSetLang = window.setLang;
  window.setLang = function(code) {
    origSetLang(code);
    renderLegalContent(type);
  };

  // Close lang menu on outside click
  document.addEventListener('click', e => {
    const sw = document.querySelector('.lang-switcher');
    if (sw && !sw.contains(e.target)) {
      const menu = document.getElementById('langMenu');
      if (menu) menu.classList.remove('open');
    }
  });
}

function renderLegalContent(type) {
  const el = document.getElementById('legalContent');
  if (!el) return;

  const db = type === 'terms' ? TERMS_CONTENT : PRIVACY_CONTENT;
  const sections = db[currentLang] || db['en'];
  el.innerHTML = buildSectionsHTML(sections);
}
